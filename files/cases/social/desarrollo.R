# Proceso de Construcción de un Tablero Customer Journey

Cada cliente presenta requerimientos
funcionales distintos para la 
construcción de un tablero Customer 
Journey. Estos requerimientos funcionales
son identificados en un proceso inicial 
de  levantamiento de información. De
nuestros tableros Customer Journey
hacemos énfasis en las siguientes 
funciones:

* Segmentar clientes objetivo.
* Medir interacciones de clientes 
objetivo con redes sociales.
* Analizar sentimientos de los clientes
(positivos, negativos, neutros),
asociados a sus temas de interés.


El diseño y desarrollo del software con sus 
respectivas pruebas de validación se realizan 
con el propósito de garantizar dichos requerimientos
funcionales. Una vez llevados a cabo, tiene lugar
el lanzamiento o puesta en funcionamiento del 
Tablero Customer Journey.

Asimismo, a través de nuestras alternativas de
soporte, nuestros clientes tienen la posibilidad
de modificar o actualizar sus tableros 
Customer Journey para responder a nuevos 
requerimientos del entorno.

